package com.shtel.mktrescenter.biz.ui.binder;

import java.util.Date;

/**
 * Created by yangyang on 2018/1/18.
 */
public class DateRange {
    private Date start;
    private Date end;

    public DateRange() {
    }

    public Date getStart() {
        return this.start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public Date getEnd() {
        return this.end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
}
